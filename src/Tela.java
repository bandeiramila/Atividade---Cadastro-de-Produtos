    private void editarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editarMouseClicked
        int posicao = tabela.getSelectedRow();
        listadeprodutos.get(posicao).setNome(txtNome.getText());
        listadeprodutos.get(posicao).setPreco(Double.parseDouble(txtPreco.getText()));
        
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        
        modelo.setNumRows(0);
        
        for(int i = 0; i<listadeprodutos.size(); i++){
            modelo.addRow(new Object[]{
                listadeprodutos.get(i).getNome(),
                listadeprodutos.get(i).getPreco()});
        }
    }//GEN-LAST:event_editarMouseClicked

    private void excluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_excluirMouseClicked
        int posicao = tabela.getSelectedRow();
        listadeprodutos.remove(posicao);
        
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        
        modelo.setNumRows(0);
        
        for(int i = 0; i<listadeprodutos.size(); i++){
            modelo.addRow(new Object[]{
                    listadeprodutos.get(i).getNome(),
                    listadeprodutos.get(i).getPreco()});
        }
    }//GEN-LAST:event_excluirMouseClicked

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        int posicao = tabela.getSelectedRow();
        
        txtNome.setText(tabela.getValueAt(posicao, 0).toString());
        txtPreco.setText(tabela.getValueAt(posicao, 1).toString());
    }//GEN-LAST:event_tabelaMouseClicked

